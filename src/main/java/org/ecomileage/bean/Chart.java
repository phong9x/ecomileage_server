package org.ecomileage.bean;

public class Chart {
	private Float totalMileage;
	private Float totalUserMileage;
	private String title;
	
	public Float getTotalMileage() {
		return totalMileage;
	}
	public void setTotalMileage(Float totalMileage) {
		this.totalMileage = totalMileage;
	}
	public Float getTotalUserMileage() {
		return totalUserMileage;
	}
	public void setTotalUserMileage(Float totalUserMileage) {
		this.totalUserMileage = totalUserMileage;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	
}
